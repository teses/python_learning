#
#   Mit Variablen rechnen
#
zahl1 = 10
zahl2 = 5

# integer addieren
ergebnis = zahl1 + zahl2
print(ergebnis)

# multiplizieren
print(zahl1 * zahl2)

# exponent
print(zahl1 ** zahl2)

# division
div = zahl1 / zahl2
print(div)
print(type(div))

# modulo
mod = 10 % 3
print("modulo", mod)

# division und komma abschneiden
div2 = 10 // 3
print(div2)
print(type(div2))


